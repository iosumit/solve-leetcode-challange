/**
 * @param {character[][]} grid
 * @return {number}
 */
var numIslands = function (grid) {
    let rows = grid.length;
    let cols = grid[0].length;
    // let m = new Set();

    function fill(i, j) {
        if (i < 0 || j < 0 || i >= rows || j >= cols || grid[i][j] == "0") {
            return;
        }
        grid[i][j] = "0";
        // m.add(i + " " + j);
        fill(i + 1, j);
        fill(i - 1, j);
        fill(i, j + 1);
        fill(i, j - 1);
    }

    let islands = 0;

    for (let i = 0; i < rows; i++) {
        for (let j = 0; j < cols; j++) {
            if (grid[i][j] == "1") {
                islands += 1;
                fill(i, j);
            }
        }
    }
    return islands;

};
